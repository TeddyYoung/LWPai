package com.yfzx.lwpai.entity;

public class Share {

	private int head;// 头像
	private String phone;// 电话
	private String praise;// 点赞
	private int winImg;// 中奖图片
	private String winTitle;// 中奖标题
	private String winSpeak;// 中奖发言
	private String date;// 中奖时间

	public int getHead() {
		return head;
	}

	public void setHead(int head) {
		this.head = head;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getPraise() {
		return praise;
	}

	public void setPraise(String praise) {
		this.praise = praise;
	}

	public int getWinImg() {
		return winImg;
	}

	public void setWinImg(int winImg) {
		this.winImg = winImg;
	}

	public String getWinTitle() {
		return winTitle;
	}

	public void setWinTitle(String winTitle) {
		this.winTitle = winTitle;
	}

	public String getWinSpeak() {
		return winSpeak;
	}

	public void setWinSpeak(String winSpeak) {
		this.winSpeak = winSpeak;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

}
